uint8  xzzHZ64_64(uint32 x, uint32 y)
{  uint8   font_dat;
   uint8   i, j, k;
   TCOLOR  bakc;

   /* 参数过滤 */
   if( x>(GUI_LCM_XMAX-64) ) return(0);
   if( y>(GUI_LCM_YMAX-64) ) return(0);
   
   
   for(i=0; i<64; i++)      // 显示共32行改成64行
   {
      for(j=0; j<8; j++)     // 每行共8字节 64个像素/8位=8字节 
      { 
   font_dat = xzzhz[i*8+j];     
         /* 设置相应的点为color或为back_color */
    for(k=0; k<8; k++)
         {
         if( (font_dat&DCB2HEX_TAB[k])==0 )
     GUI_CopyColor(&bakc, back_color);
         else 
      GUI_CopyColor(&bakc, disp_color);
    GUI_Point(x, y, bakc);      
          x++;
   }
      } 
      y++;         
// 指向下一行
      x -= 64;        

// 恢复x值
   } 
   return(1);
}
